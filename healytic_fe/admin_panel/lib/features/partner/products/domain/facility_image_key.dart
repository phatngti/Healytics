/// JSON keys for facility image map entries.
///
/// Usage:
/// ```dart
/// {
///   FacilityImageKey.imageUrl: 'https://...',
///   FacilityImageKey.label: 'Reception Area',
/// }
/// ```
abstract final class FacilityImageKey {
  static const imageUrl = 'imageUrl';
  static const label = 'label';
}
