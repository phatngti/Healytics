class _ProductTrendPanel extends StatelessWidget {
  const _ProductTrendPanel({required this.points});

  final List<ProductTrendPoint> points;

  @override
  Widget build(BuildContext context) {
    final theme = Theme.of(context);
    final colorScheme = theme.colorScheme;

    if (points.isEmpty) {
      return const AnalyticsAsyncPanel.empty(
        title: 'No booking trend data',
        description:
            'Trend lines will appear when services '
            'accumulate demand.',
      );
    }

    final maxY = points.fold<double>(
      0,
      (maxValue, point) =>
          point.revenue / 1000 > maxValue ? point.revenue / 1000 : maxValue,
    );

    return AnalyticsPanel(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          const AnalyticsSectionHeader(
            title: 'Booking and revenue trend',
            subtitle:
                'Bookings and revenue move together '
                'across the period.',
            icon: Icons.show_chart_rounded,
          ),
          AppDimens.verticalMedium,
          Row(
            mainAxisAlignment: MainAxisAlignment.end,
            children: const [
              _LegendChip(
                color: Colors.transparent,
                label: 'Bookings',
                icon: Icons.trending_up_rounded,
              ),
              _LegendChip(
                color: Colors.transparent,
                label: 'Revenue (K VND)',
                icon: Icons.payments_outlined,
              ),
            ],
          ),
          AppDimens.verticalMedium,
          AspectRatio(
            aspectRatio: 2.2,
            child: LineChart(
              LineChartData(
                lineTouchData: LineTouchData(
                  enabled: true,
                  handleBuiltInTouches: true,
                  touchSpotThreshold: 20,
                  getTouchedSpotIndicator: (barData, spotIndexes) {
                    return spotIndexes.map((_) {
                      return TouchedSpotIndicatorData(
                        const FlLine(color: Colors.transparent),
                        FlDotData(
                          show: true,
                          getDotPainter: (spot, percent, barData, index) {
                            return FlDotCirclePainter(
                              radius: 6,
                              color: colorScheme.surface,
                              strokeWidth: 2,
                              strokeColor:
                                  barData.color ?? colorScheme.secondary,
                            );
                          },
                        ),
                      );
                    }).toList();
                  },
                  touchTooltipData: LineTouchTooltipData(
                    fitInsideHorizontally: true,
                    fitInsideVertically: true,
                    getTooltipColor: (_) => colorScheme.surface,
                    tooltipBorder: BorderSide(
                      color: colorScheme.outlineVariant,
                    ),
                    getTooltipItems: (touchedSpots) {
                      return touchedSpots.map((touchedSpot) {
                        final textStyle = theme.textTheme.labelMedium?.copyWith(
                          color: touchedSpot.bar.color,
                          fontWeight: AppDimens.fontWeightBold,
                        );
                        if (touchedSpot.barIndex == 0) {
                          return LineTooltipItem(
                            '${touchedSpot.y.toInt()}'
                            ' Bookings',
                            textStyle ?? const TextStyle(),
                          );
                        }
                        return LineTooltipItem(
                          _currency(touchedSpot.y * 1000),
                          textStyle ?? const TextStyle(),
                        );
                      }).toList();
                    },
                  ),
                ),
                minX: 0,
                maxX: (points.length - 1).toDouble(),
                minY: 0,
                maxY: maxY * 1.25,
                clipData: const FlClipData.all(),
                gridData: FlGridData(
                  show: true,
                  drawVerticalLine: false,
                  getDrawingHorizontalLine: (_) => FlLine(
                    color: colorScheme.outlineVariant,
                    strokeWidth: 1,
                    dashArray: const [4, 4],
                  ),
                ),
                borderData: FlBorderData(show: false),
                titlesData: FlTitlesData(
                  topTitles: const AxisTitles(
                    sideTitles: SideTitles(showTitles: false),
                  ),
                  rightTitles: const AxisTitles(
                    sideTitles: SideTitles(showTitles: false),
                  ),
                  leftTitles: const AxisTitles(
                    sideTitles: SideTitles(showTitles: false),
                  ),
                  bottomTitles: AxisTitles(
                    sideTitles: SideTitles(
                      showTitles: true,
                      reservedSize: 32,
                      interval: 1,
                      getTitlesWidget: (value, meta) {
                        final index = value.toInt();
                        if (!_shouldShowTrendAxisLabel(
                          value: value,
                          index: index,
                          points: points,
                        )) {
                          return const SizedBox.shrink();
                        }
                        return Padding(
                          padding: AppDimens.paddingTopSmall,
                          child: Text(
                            points[index].label,
                            style: theme.textTheme.labelMedium?.copyWith(
                              color: colorScheme.onSurfaceVariant,
                            ),
                          ),
                        );
                      },
                    ),
                  ),
                ),
                lineBarsData: [
                  LineChartBarData(
                    spots: [
                      for (var i = 0; i < points.length; i++)
                        FlSpot(i.toDouble(), points[i].bookings),
                    ],
                    isCurved: true,
                    preventCurveOverShooting: true,
                    barWidth: 3,
                    color: colorScheme.primary,
                    dotData: FlDotData(
                      show: true,
                      getDotPainter: (_, __, ___, ____) {
                        return FlDotCirclePainter(
                          radius: 4,
                          color: colorScheme.surface,
                          strokeWidth: 2,
                          strokeColor: colorScheme.primary,
                        );
                      },
                    ),
                  ),
                  LineChartBarData(
                    spots: [
                      for (var i = 0; i < points.length; i++)
                        FlSpot(i.toDouble(), points[i].revenue / 1000),
                    ],
                    isCurved: true,
                    preventCurveOverShooting: true,
                    barWidth: 3,
                    color: colorScheme.tertiary,
                    dotData: FlDotData(
                      show: true,
                      getDotPainter: (_, __, ___, ____) {
                        return FlDotCirclePainter(
                          radius: 4,
                          color: colorScheme.surface,
                          strokeWidth: 2,
                          strokeColor: colorScheme.tertiary,
                        );
                      },
                    ),
                  ),
                ],
              ),
            ),
          ),
        ],
      ),
    );
  }
}

bool _shouldShowTrendAxisLabel({
  required double value,
  required int index,
  required List<ProductTrendPoint> points,
}) {
  if (value != index.toDouble() || index < 0 || index >= points.length) {
    return false;
  }
  return index == 0 || points[index].label != points[index - 1].label;
}

