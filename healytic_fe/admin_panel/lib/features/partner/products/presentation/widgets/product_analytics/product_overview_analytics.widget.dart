/// Analytics overview for the product index page.
class ProductOverviewAnalyticsSection extends ConsumerWidget {
  const ProductOverviewAnalyticsSection({super.key});

  @override
  Widget build(BuildContext context, WidgetRef ref) {
    final analyticsAsync = ref.watch(productOverviewAnalyticsProvider);

    return analyticsAsync.when(
      data: (state) {
        return Column(
          crossAxisAlignment: CrossAxisAlignment.start,
          children: [
            AnalyticsSectionHeader(
              title: 'Service Performance',
              subtitle:
                  'Finance-style KPIs with booking, review, and catalog '
                  'health visibility.',
              icon: Icons.insights_rounded,
              trailing: AnalyticsPeriodFilter(
                selectedPeriod: state.selectedPeriod,
                onChanged: (period) {
                  ref
                      .read(productOverviewAnalyticsProvider.notifier)
                      .setTimePeriod(period);
                },
              ),
            ),
            if (state.isRefreshing) ...[
              AppDimens.verticalSmall,
              const LinearProgressIndicator(),
            ],
            AppDimens.verticalLarge,
            _ProductOverviewKpiGrid(analytics: state.analytics),
            AppDimens.verticalLarge,
            _ProductOverviewTopPanels(analytics: state.analytics),
            AppDimens.verticalLarge,
            _ProductBookingMetricsPanel(analytics: state.analytics),
          ],
        );
      },
      loading: () =>
          const AnalyticsAsyncPanel.loading(title: 'Loading product analytics'),
      error: (error, _) => AnalyticsAsyncPanel.error(
        title: 'Unable to load product analytics',
        description: error.toString(),
        action: TextButton(
          onPressed: () {
            ref.invalidate(productOverviewAnalyticsProvider);
          },
          child: const Text('Retry'),
        ),
      ),
    );
  }
}

