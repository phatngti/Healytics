class _ReviewRow extends StatelessWidget {
  const _ReviewRow({required this.bucket, required this.total});

  final ProductReviewBucket bucket;
  final int total;

  @override
  Widget build(BuildContext context) {
    final colorScheme = Theme.of(context).colorScheme;
    final ratio = total == 0 ? 0.0 : bucket.count / total;

    return Row(
      children: [
        SizedBox(width: 32, child: Text('${bucket.stars}★')),
        Expanded(
          child: ClipRRect(
            borderRadius: AppDimens.radiusPill,
            child: LinearProgressIndicator(
              value: ratio,
              minHeight: AppDimens.spaceMd,
              backgroundColor: colorScheme.surfaceContainerHighest,
            ),
          ),
        ),
        AppDimens.horizontalMedium,
        SizedBox(
          width: 40,
          child: Text(bucket.count.toString(), textAlign: TextAlign.end),
        ),
      ],
    );
  }
}

String _currency(double value) {
  final compact = value >= 1000000
      ? '${(value / 1000000).toStringAsFixed(1)}M'
      : '${(value / 1000).toStringAsFixed(0)}K';
  return '$compact₫';
}
