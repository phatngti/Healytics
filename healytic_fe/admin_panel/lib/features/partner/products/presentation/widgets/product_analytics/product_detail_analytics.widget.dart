/// Analytics section used inside the product detail page.
class ProductDetailAnalyticsSection extends ConsumerWidget {
  const ProductDetailAnalyticsSection({super.key, required this.product});

  final Product product;

  @override
  Widget build(BuildContext context, WidgetRef ref) {
    final analyticsAsync = ref.watch(
      productDetailAnalyticsProvider(product.id.value),
    );

    return analyticsAsync.when(
      data: (state) {
        return Column(
          crossAxisAlignment: CrossAxisAlignment.start,
          children: [
            AnalyticsSectionHeader(
              title: 'Performance Insights',
              subtitle:
                  'Service demand, review quality, and operational readiness '
                  'for this treatment.',
              icon: Icons.query_stats_rounded,
              trailing: AnalyticsPeriodFilter(
                selectedPeriod: state.selectedPeriod,
                onChanged: (period) {
                  ref
                      .read(
                        productDetailAnalyticsProvider(
                          product.id.value,
                        ).notifier,
                      )
                      .setTimePeriod(period);
                },
              ),
            ),
            if (state.isRefreshing) ...[
              AppDimens.verticalSmall,
              const LinearProgressIndicator(),
            ],
            AppDimens.verticalLarge,
            _ProductDetailKpiGrid(analytics: state.analytics),
            AppDimens.verticalLarge,
            _ProductDetailTopPanels(analytics: state.analytics),
            AppDimens.verticalLarge,
            _ProductReadinessPanel(analytics: state.analytics),
          ],
        );
      },
      loading: () =>
          const AnalyticsAsyncPanel.loading(title: 'Loading service analytics'),
      error: (error, _) => AnalyticsAsyncPanel.error(
        title: 'Unable to load service analytics',
        description: error.toString(),
        action: TextButton(
          onPressed: () {
            ref.invalidate(productDetailAnalyticsProvider(product.id.value));
          },
          child: const Text('Retry'),
        ),
      ),
    );
  }
}

class _ProductDetailKpiGrid extends StatelessWidget {
