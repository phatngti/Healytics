  final ProductDetailAnalytics analytics;

  @override
  Widget build(BuildContext context) {
    return LayoutBuilder(
      builder: (context, constraints) {
        final crossAxisCount = constraints.maxWidth >= 1200
            ? 4
            : constraints.maxWidth >= 720
            ? 2
            : 1;

        return GridView.count(
          crossAxisCount: crossAxisCount,
          shrinkWrap: true,
          physics: const NeverScrollableScrollPhysics(),
          crossAxisSpacing: AppDimens.spaceLg,
          mainAxisSpacing: AppDimens.spaceLg,
          childAspectRatio: 1.15,
          children: [
            AnalyticsKpiCard(
              label: 'Bookings',
              value: analytics.bookings.toString(),
              helper: 'Completed sessions in the selected period',
              icon: Icons.calendar_month_rounded,
              trend: analytics.bookingsDelta,
              trendPositive: true,
            ),
            AnalyticsKpiCard(
              label: 'Revenue',
              value: _currency(analytics.revenue),
              helper: 'Revenue generated by this service',
              icon: Icons.payments_rounded,
              trend: analytics.revenueDelta,
              trendPositive: true,
            ),
            AnalyticsKpiCard(
              label: 'Completion rate',
              value: '${analytics.completionRate.toStringAsFixed(1)}%',
              helper: 'Booking outcome quality for this service',
              icon: Icons.task_alt_rounded,
              trend: analytics.completionRateDelta,
              trendPositive: true,
            ),
            AnalyticsKpiCard(
              label: 'Average rating',
              value: '${analytics.averageRating.toStringAsFixed(1)}/5',
              helper: '${analytics.reviewCount} submitted reviews',
              icon: Icons.star_rounded,
              trend: 1.6,
              trendPositive: true,
              badgeLabel: 'Reviews',
              badgeTone: AnalyticsStatusTone.warning,
            ),
          ],
        );
      },
    );
  }
}

class _ProductDetailTopPanels extends StatelessWidget {
  const _ProductDetailTopPanels({required this.analytics});

  final ProductDetailAnalytics analytics;

  @override
  Widget build(BuildContext context) {
    return LayoutBuilder(
      builder: (context, constraints) {
        final isWide = constraints.maxWidth >= 1100;

        if (!isWide) {
          return Column(
            children: [
              _DetailTrendPanel(points: analytics.trendPoints),
              AppDimens.verticalLarge,
              _ReviewQualityPanel(analytics: analytics),
